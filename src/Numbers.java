public class Numbers {

    public static void main(String[] args) {

        int num = 7;

        if (num == 0) {
            System.out.println("Selected number is zero");
        } else if (num == 1) {
            System.out.println("Selected number is one");
        } else if (num == 2) {
            System.out.println("Selected number is two");
        } else if (num == 3) {
            System.out.println("Selected number is three");
        } else if (num == 4) {
            System.out.println("Selected number is four");
        } else if (num == 5) {
            System.out.println("Selected number is five");
        } else if (num == 6) {
            System.out.println("Selected number is six");
        } else if (num == 7) {
            System.out.println("Selected number is seven");
        } else if (num == 8) {
            System.out.println("Selected number is eight");
        } else if (num == 9) {
            System.out.println("Selected number is nine");
        } else {
            System.out.println("error");
        }
    }
}
